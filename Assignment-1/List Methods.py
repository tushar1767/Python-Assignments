list = [2, 1, 3]

# Reverses the list

list.reverse()
print(list)

# Inserting at any position

list.insert(2,4)
print(list)

# Deleting any value

list.remove(4)
print(list)

# Add Element to end

list.append(4)
print(list)

# Delet any index

list.pop(3)
print(list)

# Sort in Ascending Order

list.sort()
print(list)

# Sort in Descending Order

list.sort(reverse=True)
print(list)
