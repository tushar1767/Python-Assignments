# We Can Create Lists Of : Str, Float, Int

marks = [83, 62, 49, 59, 93]
foods = ["Samosa", "Burger", "Vadapav", "Pizza"]

# Printing A Lists
print(marks)
print(foods)

# Printing Length
print(len(marks))
print(len(foods))

# Indexing (Printing Position)
print(marks[3])
print(foods[2])
